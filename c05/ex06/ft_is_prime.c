int	ft_is_prime(int nb)
{
	int	i;
	int	v;

	i = 1;
	v = 0;
	while (i <= nb)
	{
		if (nb % i == nb || nb % i == 0)
			v++;
		i++;
	}
	if (v == 2)
		return (1);
	else
		return (0);
}
