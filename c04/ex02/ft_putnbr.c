#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	v;

	if (nb == -2147483648)
	{
		write(1, "-", 1);
		write(1, "2", 1);
		ft_putnbr(147483648);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
	}
	v = (nb % 10) + '0';
	write(1, &v, 1);
}
