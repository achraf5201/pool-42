int	ft_atoi(char *str)
{
	int	i;
	int	v;
	int	nb;

	i = 0;
	v = 1;
	nb = 0;
	while ((str[i] != '\0' && str[i] == ' ')
		|| (str[i] != '\0' && str[i] == 9 && str[i] == 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			v *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + str[i] - '0';
		i++;
	}
	return (nb * v);
}
