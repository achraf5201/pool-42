char	*ft_strcapitalize(char *str)
{
	int	i;
	int	v;

	i = 0;
	v = 1;
	while (str[i])
	{
		if ((str[i] >= 'a' && str[i] <= 'z') && v == 1)
			str[i] -= 32;
		else if ((str[i] >= 'A' && str[i] <= 'Z') && v == 0)
			str[i] += 32;
		if ((str[i] >= 'a' && str[i] <= 'z')
			|| (str[i] >= 'A' && str[i] <= 'Z')
			|| (str[i] >= '0' && str[i] <= '9'))
			v = 0;
		else
			v = 1;
		i++;
	}
	return (str);
}
